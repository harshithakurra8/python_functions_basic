# Write a Python program to define a string containing
#  special characters in various forms


special_str1 = "Hello\tWorld!\nWelcome to Python."

special_str2 = "She said, \"Python is amazing!\""

special_str3 = "This is a backslash: \\"

special_str4 = r"This is a raw string: C:\Users\Name\Documents"

special_str5 = "Unicode examples: \u2600 \u2764 \U0001F600" 

print("Special String 1:", special_str1)
print("Special String 2:", special_str2)
print("Special String 3:", special_str3)
print("Special String 4:", special_str4)
print("Special String 5:", special_str5)
